print("Hello, welcome to a simple program to calculate the wavelength of a wave given the speed and frequency")
v = eval(input("Please input the speed in meters per second: "))
f = eval(input("Please input the frequency in Hertz(cycles per second): "))
wavelength = v/f
print("The speed (m/s):", v)
print("The frequency(Hz):", f)
print("The wavelength(m):", wavelength)
